/**
 * @author entropyfeng
 * 支持参数类型
 */
public enum SupportPara {
    /**
     * {@link String}
     */
    STRING,
    /**
     * {@link Integer}
     */
    INTEGER,
    /**
     * {@link Long}
     */
    LONG,
    /**
     * {@link Double}
     */
    DOUBLE,
    /**
     * 使用字符串表示的数
     */
    NUMBER
}
